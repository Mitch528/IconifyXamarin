        public static readonly List<KeyValuePair<char, string>> Characters;

        public static IIcon[] Icons { get; }

        static FontAwesomeIcons()
        {
            Characters = IconReflectionUtils.GetIcons<FontAwesomeIcons>();
            Icons = Characters.Select(p => new FontAwesomeIcons(p.Value, p.Key)).Cast<IIcon>().ToArray();
        }

        private readonly string _key;

        public FontAwesomeIcons(string key, char @char)
        {
            _key = key;
            Character = @char;
        }

        public string Key => Characters.Single(p => p.Value == _key).Value.Replace("_", "-");


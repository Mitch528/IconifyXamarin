        public static readonly List<KeyValuePair<char, string>> Characters;

        public static IIcon[] Icons { get; }

        static TypiconsIcons()
        {
            Characters = IconReflectionUtils.GetIcons<TypiconsIcons>();
            Icons = Characters.Select(p => new TypiconsIcons(p.Value, p.Key)).Cast<IIcon>().ToArray();
        }

        private readonly string _key;

        public TypiconsIcons(string key, char @char)
        {
            _key = key;
            Character = @char;
        }

        public string Key => Characters.Single(p => p.Value == _key).Value.Replace("_", "-");

        public char Character { get; }

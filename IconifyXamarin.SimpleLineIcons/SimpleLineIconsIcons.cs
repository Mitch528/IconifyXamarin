        public static readonly List<KeyValuePair<char, string>> Characters;

        public static IIcon[] Icons { get; }

        static SimpleLineIconsIcons()
        {
            Characters = IconReflectionUtils.GetIcons<SimpleLineIconsIcons>();
            Icons = Characters.Select(p => new SimpleLineIconsIcons(p.Value, p.Key)).Cast<IIcon>().ToArray();
        }

        private readonly string _key;

        public SimpleLineIconsIcons(string key, char @char)
        {
            _key = key;
            Character = @char;
        }

        public string Key => Characters.Single(p => p.Value == _key).Value.Replace("_", "-");

        public char Character { get; }
